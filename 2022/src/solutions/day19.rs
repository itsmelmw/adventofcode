fn parse(input: &str) -> Vec<usize> {
    return vec![];
}

fn solve1(parsed: &Vec<usize>) -> String {
    return 0.to_string();
}

fn solve2(parsed: &Vec<usize>) -> String {
    return 0.to_string();
}

pub fn solve(input: &str) -> (String, String) {
    let parsed = parse(input);
    return (solve1(&parsed), solve2(&parsed));
}
